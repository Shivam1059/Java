    public void loadAttendanceData(JTable table) {
    try (Connection con = GetConnect.getConnection()) {

        String query = "SELECT name, student_id, batch, date, status FROM attendance ORDER BY date DESC";

        // Prepare statement
        PreparedStatement pst = con.prepareStatement(query);
        ResultSet rs = pst.executeQuery();

        // Get table model
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); // clear existing data

        // Loop through the results and add each record to the table
        while (rs.next()) {
            String name = rs.getString("name");
            int studentId = rs.getInt("student_id");
            String batch = rs.getString("batch");
            var date = rs.getDate("date"); // java.sql.Date
            String status = rs.getString("status");

            model.addRow(new Object[]{name, studentId, batch, date, status});
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading attendance data: " + e.getMessage());
    }
}


    
