    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String selectedBatch = jComboBox1.getSelectedItem().toString();

    // Get the table model sorter
    TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTable1.getModel());
    jTable1.setRowSorter(sorter);

    if (selectedBatch.equalsIgnoreCase("All")) {
        // Show all records
        sorter.setRowFilter(null);
    } else {
        // Filter rows where Batch column matches the selected batch
        // Assuming "Batch" is in column index 2 (change if needed)
        sorter.setRowFilter(RowFilter.regexFilter(selectedBatch, 2));
    }        
    }//GEN-LAST:event_jComboBox1ActionPerformed

