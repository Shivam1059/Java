    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        //update all table of attendance   
           DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        String updateQuery = "UPDATE attendance SET date = ?, status = ? WHERE student_id = ?";

        try (Connection con = GetConnect.getConnection();
             PreparedStatement pst = con.prepareStatement(updateQuery)) {

            for (int i = 0; i < model.getRowCount(); i++) {
                Integer id = Integer.valueOf(model.getValueAt(i, 1).toString());
                String date = model.getValueAt(i, 3).toString();   // column index for date
                String status = model.getValueAt(i, 4).toString(); // column index for status

                pst.setString(1, date);
                pst.setString(2, status);
                pst.setInt(3, id);

                pst.executeUpdate();
            }

            JOptionPane.showMessageDialog(null, "All rows updated successfully!");

        } catch (SQLException ex) {
           ex.printStackTrace();   
        }
    
         
    }//GEN-LAST:event_jButton8ActionPerformed

